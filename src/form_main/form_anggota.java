    private void btncari2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncari2ActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No. ");
        model.addColumn("Kode. ");
        model.addColumn("Nama. ");
        model.addColumn("Alamat. ");
        model.addColumn("Tanggal Lahir. ");
        model.addColumn("Jenis Kelamin. ");
        model.addColumn("Jabatan. ");
        try{
            int no = 1;
            Connection TugasVisual = DriverManager.getConnection("jdbc:mysql://localhost/koperasi","root","");
            ResultSet rs = TugasVisual.createStatement().executeQuery("select * from anggota where kode_agt like '%" + txtcari2.getText() + "%'" +
                "or nama_agt like '%" + txtcari2.getText() + "%'" + "or alamat_agt like '%" + txtcari2.getText() + "%'" );
            while (rs.next()) {
                model.addRow(new Object[]{no++,
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                });

            }
            tabelanggota.setModel(model);
            kosongkan_form();
        }catch (Exception e){

        }
    }//GEN-LAST:event_btncari2ActionPerformed

