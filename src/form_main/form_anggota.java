      private java.util.Date getTanggalFromTableAnggota(JTable tabelanggota, int i) {
        JTable tabel = tabelanggota;
        String str_tgl = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), i));
        java.util.Date tanggal = null;
        try {
            tanggal = new SimpleDateFormat("dd-MM-yyyy").parse(str_tgl);
        } catch (ParseException ex) {
            Logger.getLogger(form_anggota.class.getName()).log(Level.SEVERE, null, ex);
        }
        return tanggal;
    }
    
